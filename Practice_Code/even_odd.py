# classify between even numbers and odd numbers using if elif statements.
# modulus (% operator) returns the reminder after division between 2 numbers.

number = int(input("Enter the number: "))

if number%2 == 1:    # number = 15, 15%2 = 1
    print("The number is Odd")

elif number%2 == 0:   # number = 16, 16%2 = 0
    print("The number is even")


